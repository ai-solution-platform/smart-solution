function findQuickResponse(message: string): string | null {
  const lowerMessage = message.toLowerCase();
  for (const [keyword, response] of Object.entries(QUICK_RESPONSES)) {
    if (lowerMessage.includes(keyword)) {
      return response;
    }
  }
  return null;
}

// ---------------------------------------------------------------------------
// POST - Handle chat messages
// ---------------------------------------------------------------------------

export async function POST(request: NextRequest) {
  try {
    const body: ChatRequestBody = await request.json();

    if (!body.message || body.message.trim().length === 0) {
      return NextResponse.json(
        { error: "กรุณาพิมพ์ข้อความ" },
        { status: 400 }
      );
    }

    const userMessage = body.message.trim();

    // Check for quick response first
    const quickResponse = findQuickResponse(userMessage);

    if (quickResponse) {
      return NextResponse.json({
        role: "assistant" as const,
        content: quickResponse,
      });
    }

    // Default helpful response for unmatched queries
    const defaultResponse = `ขอบคุณที่ติดต่อมา สำหรับคำถามเกี่ยวกับ "${userMessage}" แนะนำให้ท่านติดต่อหน่วยงานโดยตรงเพื่อข้อมูลที่ถูกต้องและเป็นปัจจุบัน

ท่านสามารถ:
1. โทรศัพท์ติดต่อสำนักงาน ในเวลาราชการ 08:30-16:30 น.
2. ส่งข้อความผ่านแบบฟอร์มติดต่อบนเว็บไซต์
3. เข้ามาติดต่อด้วยตนเองที่สำนักงาน

หากต้องการความช่วยเหลือเรื่องอื่น สามารถสอบถามได้เลยครับ/ค่ะ`;

    return NextResponse.json({
      role: "assistant" as const,
      content: defaultResponse,
    });
  } catch (error) {
    console.error("POST /api/chat error:", error);
    return NextResponse.json(
      { error: "เกิดข้อผิดพลาดในระบบแชท" },
      { status: 500 }
    );
  }
}
